package SGM

import (
	"fmt"
)

func Additionize(num1 float64, num2 float64) {
	fmt.Println(num1 + num2)
}

func Subtractionize(num1 float64, num2 float64) {
	fmt.Println(num1 - num2)
}

func Multiplicationize(num1 float64, num2 float64) {
	fmt.Println(num1 * num2)
}

func Divizonize(num1 float64, num2 float64) {
	fmt.Println(num1 / num2)
}

func Moduloize(num1 int, num2 int) {
	fmt.Println(num1 % num2)
}
